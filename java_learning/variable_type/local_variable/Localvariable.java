package variable_type.local_variable;

public class Localvariable {
    public static void main(String[] args) {
        // local variable:- 
        /* variables that are assigned inside the method are called are local variable
        * other method doesn't know about this variable other then the method 
        * in which they are created */
       int localvariable = 2224;
       System.out.println("this is an example of localvariable " + localvariable);
    }
}